// 使えるコマンドの一覧を表示する
// ==========================================

/**
 * ヘルプメッセージを返す
 * @returns {object} LINE メッセージオブジェクト
 */
async function execute() {
    const helpText = [
        "📖 使えるコマンド一覧",
        "",
        "── 再生 ──",
        "",
        "🔹 再生（/play）",
        "　→ ランダムにエピソードを再生",
        "",
        "── チャンネル管理 ──",
        "",
        "🔹 登録 [番組名]（/add）",
        "　→ チャンネルを検索して追加",
        "　例: 登録 ゆる言語学ラジオ",
        "　※ YouTube URLも可",
        "",
        "🔹 一覧（/list）",
        "　→ 登録チャンネルを表示",
        "",
        "🔹 削除 [番号]（/remove）",
        "　→ チャンネルを削除",
        "",
        "── システム ──",
        "",
        "🔹 ヘルプ（/help）",
        "🔹 確認（/ping）",
        "🔹 状態（/status）",
        "",
        "─────────────────",
        "上記以外のメッセージはオウム返しします 🦜",
    ].join("\n");

    return {
        type: "text",
        text: helpText,
    };
}

module.exports = { execute };
