/**
 * ヘルプメッセージを返す
 * @returns {object} LINE メッセージオブジェクト
 */
async function execute() {
    const helpText = [
        "📖 使えるコマンド一覧",
        "",
        "── 再生 ──",
        "",
        "🔹 再生（/play）",
        "　→ ランダムにエピソードを再生",
        "",
        "── スケジュール ──",
        "",
        "🔹 スケジュール [時間]（/schedule）",
        "　→ 毎日の自動再生を設定",
        "　例: スケジュール 7",
        "　解除: スケジュール 解除",
        "",
        "── チャンネル管理 ──",
        "",
        "🔹 登録 [番組名]（/add）",
        "　→ チャンネルを検索して追加",
        "",
        "🔹 一覧（/list）",
        "　→ 登録チャンネルを表示",
        "",
        "🔹 削除 [番号]（/remove）",
        "　→ チャンネルを削除",
        "",
        "── システム ──",
        "",
        "🔹 ヘルプ / 確認 / 状態",
        "",
        "─────────────────",
        "上記以外のメッセージはオウム返しします 🦜",
    ].join("\n");

    return {
        type: "text",
        text: helpText,
    };
}

module.exports = { execute };
