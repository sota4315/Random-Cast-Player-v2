// ==========================================
// /help（ヘルプ）コマンド
// 使えるコマンドの一覧を表示する
// ==========================================

/**
 * ヘルプメッセージを返す
 * @returns {object} LINE メッセージオブジェクト
 */
function execute() {
    const helpText = [
        "📖 使えるコマンド一覧",
        "",
        "🔹 ヘルプ（/help）",
        "　→ この一覧を表示します",
        "",
        "🔹 確認（/ping）",
        "　→ Botが動いているか確認します",
        "",
        "🔹 状態（/status）",
        "　→ Botのバージョン情報を表示します",
        "",
        "─────────────────",
        "上記以外のメッセージはオウム返しします 🦜",
    ].join("\n");

    return {
        type: "text",
        text: helpText,
    };
}

module.exports = { execute };
