// ==========================================
// /help（ヘルプ）コマンド
// 使えるコマンドの一覧を表示する
// ==========================================

/**
 * ヘルプメッセージを返す
 * @returns {object} LINE メッセージオブジェクト
 */
async function execute() {
    const helpText = [
        "📖 使えるコマンド一覧",
        "",
        "🔹 ヘルプ（/help）",
        "　→ この一覧を表示します",
        "",
        "── チャンネル管理 ──",
        "",
        "🔹 登録 [URL]（/add）",
        "　→ チャンネルを追加します",
        "　例: 登録 https://example.com/feed.xml",
        "",
        "🔹 一覧（/list）",
        "　→ 登録チャンネルを表示します",
        "",
        "🔹 削除 [番号]（/remove）",
        "　→ チャンネルを削除します",
        "　例: 削除 1",
        "",
        "── システム ──",
        "",
        "🔹 確認（/ping）",
        "　→ Bot動作確認",
        "",
        "🔹 状態（/status）",
        "　→ バージョン・DB情報",
        "",
        "─────────────────",
        "上記以外のメッセージはオウム返しします 🦜",
    ].join("\n");

    return {
        type: "text",
        text: helpText,
    };
}

module.exports = { execute };
